/* ====== Framebuffer & palette ====== */
uint8_t gfx_fb[GFX_FB_SIZE];

/* 默认 16 色调色板 (RGB565)。索引 0 = 黑（背景）。
 * GameBoy 4 阶灰 + 标准 12 色，可按需修改 */
#define RGB565(r,g,b) ((uint16_t)(((r)&0xF8)<<8 | ((g)&0xFC)<<3 | ((b)&0xF8)>>3))

uint16_t gfx_palette[16] = {
    RGB565(  0,   0,   0),   /* 0  black       */
    RGB565( 85,  85,  85),   /* 1  dark gray   */
    RGB565(170, 170, 170),   /* 2  light gray  */
    RGB565(255, 255, 255),   /* 3  white       */
    RGB565(255,   0,   0),   /* 4  red         */
    RGB565(  0, 255,   0),   /* 5  green       */
    RGB565(  0,   0, 255),   /* 6  blue        */
    RGB565(255, 255,   0),   /* 7  yellow      */
    RGB565(  0, 255, 255),   /* 8  cyan        */
    RGB565(255,   0, 255),   /* 9  magenta     */
    RGB565(255, 128,   0),   /* 10 orange      */
    RGB565(128,   0, 255),   /* 11 purple      */
    RGB565(  0, 128,   0),   /* 12 dark green  */
    RGB565(  0,   0, 128),   /* 13 navy        */
    RGB565(128, 128,   0),   /* 14 olive       */
    RGB565( 64,  64,  64),   /* 15 charcoal    */
};

void GFX_Init(void)
{
    memset(gfx_fb, 0, sizeof gfx_fb);
}

/* ====== 像素层 ======
 * 打包方式：高 nibble = 偶数 x (左)，低 nibble = 奇数 x (右)
 * byte_idx = y * (W/2) + x/2
 */
static inline int fb_idx(int x, int y) { return y * (GFX_W / 2) + (x >> 1); }

void GFX_SetPixel(int x, int y, gfx_color_t c)
{
    if ((unsigned)x >= GFX_W || (unsigned)y >= GFX_HEIGHT) return;
    uint8_t *p = &gfx_fb[fb_idx(x, y)];
    if (x & 1) *p = (*p & 0xF0) | (c & 0x0F);
    else       *p = (*p & 0x0F) | ((c & 0x0F) << 4);
}

gfx_color_t GFX_GetPixel(int x, int y)
{
    if ((unsigned)x >= GFX_W || (unsigned)y >= GFX_HEIGHT) return 0;
    uint8_t b = gfx_fb[fb_idx(x, y)];
    return (x & 1) ? (b & 0x0F) : (b >> 4);
}

void GFX_Clear(gfx_color_t c)
{
    uint8_t b = ((c & 0x0F) << 4) | (c & 0x0F);
    memset(gfx_fb, b, sizeof gfx_fb);
}

/* ====== 矩形 / 线 ====== */
void GFX_DrawHLine(int x, int y, int w, gfx_color_t c)
{
    if ((unsigned)y >= GFX_HEIGHT) return;
    if (x < 0) { w += x; x = 0; }
    if (x + w > GFX_W) w = GFX_W - x;
    if (w <= 0) return;
    /* 简单：逐像素。优化空间是按 byte 批量填，留给后面 */
    for (int i = 0; i < w; i++) GFX_SetPixel(x + i, y, c);
}

void GFX_DrawVLine(int x, int y, int h, gfx_color_t c)
{
    if ((unsigned)x >= GFX_W) return;
    if (y < 0) { h += y; y = 0; }
    if (y + h > GFX_HEIGHT) h = GFX_HEIGHT - y;
    if (h <= 0) return;
    for (int i = 0; i < h; i++) GFX_SetPixel(x, y + i, c);
}

void GFX_FillRect(int x, int y, int w, int h, gfx_color_t c)
{
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > GFX_W) w = GFX_W - x;
    if (y + h > GFX_HEIGHT) h = GFX_HEIGHT - y;
    if (w <= 0 || h <= 0) return;
    for (int j = 0; j < h; j++)
        for (int i = 0; i < w; i++)
            GFX_SetPixel(x + i, y + j, c);
}

void GFX_DrawRect(int x, int y, int w, int h, gfx_color_t c)
{
    GFX_DrawHLine(x, y, w, c);
    GFX_DrawHLine(x, y + h - 1, w, c);
    GFX_DrawVLine(x, y, h, c);
    GFX_DrawVLine(x + w - 1, y, h, c);
}

/* Bresenham */
void GFX_DrawLine(int x0, int y0, int x1, int y1, gfx_color_t c)
{
    int dx = x1 - x0, dy = y1 - y0;
    int sx = dx < 0 ? -1 : 1;
    int sy = dy < 0 ? -1 : 1;
    dx = dx < 0 ? -dx : dx;
    dy = dy < 0 ? -dy : dy;
    int err = dx - dy;
    for (;;) {
        GFX_SetPixel(x0, y0, c);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

/* 中点圆 */
void GFX_DrawCircle(int cx, int cy, int r, gfx_color_t c)
{
    int x = r, y = 0;
    int err = 1 - r;
    while (x >= y) {
        GFX_SetPixel(cx + x, cy + y, c);
        GFX_SetPixel(cx + y, cy + x, c);
        GFX_SetPixel(cx - y, cy + x, c);
        GFX_SetPixel(cx - x, cy + y, c);
        GFX_SetPixel(cx - x, cy - y, c);
        GFX_SetPixel(cx - y, cy - x, c);
        GFX_SetPixel(cx + y, cy - x, c);
        GFX_SetPixel(cx + x, cy - y, c);
        y++;
        if (err < 0) err += 2 * y + 1;
        else { x--; err += 2 * (y - x) + 1; }
    }
}

void GFX_FillCircle(int cx, int cy, int r, gfx_color_t c)
{
    int x = r, y = 0;
    int err = 1 - r;
    while (x >= y) {
        GFX_DrawHLine(cx - x, cy + y, 2 * x + 1, c);
        GFX_DrawHLine(cx - x, cy - y, 2 * x + 1, c);
        GFX_DrawHLine(cx - y, cy + x, 2 * y + 1, c);
        GFX_DrawHLine(cx - y, cy - x, 2 * y + 1, c);
        y++;
        if (err < 0) err += 2 * y + 1;
        else { x--; err += 2 * (y - x) + 1; }
    }
}

