void GFX_DrawChar(int x, int y, char ch, gfx_color_t fg, gfx_color_t bg, uint8_t size)
{
    if (size == 0) size = 1;
    const uint8_t *g = &glcd_font[(uint8_t)ch * 5];
    /* 5 列 + 1 空列 = 6 列；每列 8 位 */
    for (int col = 0; col < 6; col++) {
        uint8_t bits = (col < 5) ? g[col] : 0x00;
        for (int row = 0; row < 8; row++) {
            gfx_color_t c = (bits & (1 << row)) ? fg : bg;
            if (c == 0xFF) continue;          /* 让 bg=0xFF 表示透明背景 */
            if (size == 1) {
                GFX_SetPixel(x + col, y + row, c);
            } else {
                GFX_FillRect(x + col * size, y + row * size, size, size, c);
            }
        }
    }
}

void GFX_DrawString(int x, int y, const char *s, gfx_color_t fg, gfx_color_t bg, uint8_t size)
{
    if (size == 0) size = 1;
    int cx = x;
    while (*s) {
        if (*s == '\n') { y += 8 * size; cx = x; }
        else { GFX_DrawChar(cx, y, *s, fg, bg, size); cx += 6 * size; }
        s++;
        if (cx + 6 * size > GFX_W) { cx = x; y += 8 * size; }
    }
}

/* Sprite: 4bpp 打包，高 nibble = 左像素 */
void GFX_DrawSprite(int x, int y, int w, int h, const uint8_t *data,
                    gfx_color_t transparent)
{
    int stride = (w + 1) / 2;
    for (int j = 0; j < h; j++) {
        for (int i = 0; i < w; i++) {
            uint8_t b = data[j * stride + (i >> 1)];
            gfx_color_t c = (i & 1) ? (b & 0x0F) : (b >> 4);
            if (c == transparent) continue;
            GFX_SetPixel(x + i, y + j, c);
        }
    }
}

/* ====== Flush ======
 * 按行展开 4bpp -> RGB565，调 display 层一次发一行
 */
void GFX_Flush(void)
{
	static uint16_t line[GFX_W];
    for (int y = 0; y < GFX_HEIGHT; y++) {
        const uint8_t *src = &gfx_fb[y * (GFX_W / 2)];
        for (int x = 0; x < GFX_W; x += 2) {
            uint8_t b = *src++;
            line[x]     = gfx_palette[b >> 4];
            line[x + 1] = gfx_palette[b & 0x0F];
        }
        Display_BlitRect(0, y, GFX_W, 1, line);
    }
}
